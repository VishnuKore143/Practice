package Practices;

public class ArithmeticOperators {
	public static void main(String[]args) {
		
		int a = 9;
		int b = 1;
		System.out.println("The + is the arithmetic operator 9+1 = "+a+b);
		
		int x = 10;
		int y = 8;
		System.out.println("The - is the arithmetic operator 10-8 = "+(x-y));
		
		int k = 9;
		int j = 1;
		System.out.println("The * is the arithmetic operator 9*1 = "+k*j);
		
		int i = 9;
		int p = 1;
		System.out.println("The / is the arithmetic operator 9/1 = "+i/p);
		
		int c = 9;
		int d = 1;
		System.out.println("The % is the arithmetic operator 9%1 = "+c%d);
		
		
	}

}
